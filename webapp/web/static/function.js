function getQueryString(name) {
    var reg = new RegExp("(^|&)" + name + "=([^&]*)(&|$)", "i");
    var r = window.location.search.substr(1).match(reg);
    if (r != null) return unescape(r[2]); return null;
}
function clone(obj) {
    var o;
    if (typeof obj == "object") {
        if (obj === null) {
            o = null;
        } else {
            if (obj instanceof Array) {
                o = [];
                for (var i = 0, len = obj.length; i < len; i++) {
                    o.push(clone(obj[i]));
                }
            } else {
                o = {};
                for (var k in obj) {
                    o[k] = clone(obj[k]);
                }
            }
        }
    } else {
        o = obj;
    }
    return o;
}
function getByteLength(str) {
    return str.replace(/[^\x00-\xff]/g, "**").length;
}
function formValidate(form) {
    var errors = [];
    var requireds = form.$error.required;
    var patterns = form.$error.pattern;
    var parses = form.$error.parse;
    var mins = form.$error.min;
    if (requireds) {
        for (var i = 0; i < requireds.length; i++) {
            var obj = requireds[i];
            var errmsg = document.getElementsByName(obj.$name)[0].getAttribute("errormsg");
            errors.push({ msg: errmsg });
        }
    }
    if (patterns) {
        for (var i = 0; i < patterns.length; i++) {
            var obj = patterns[i];
            var errmsg = document.getElementsByName(obj.$name)[0].getAttribute("errormsg");
            errors.push({ msg: errmsg });
        }
    }
    if (parses) {
        for (var i = 0; i < parses.length; i++) {
            var obj = parses[i];
            var errmsg = document.getElementsByName(obj.$name)[0].getAttribute("errormsg");
            errors.push({ msg: errmsg });
        }
    }
    if (mins) {
        for (var i = 0; i < mins.length; i++) {
            var obj = mins[i];
            var errmsg = document.getElementsByName(obj.$name)[0].getAttribute("errormsg");
            errors.push({ msg: errmsg });
        }
    }
    return errors;
}

